# ======= String =======
# Python 은 String 에 “double quotes”, ‘single quotes’ 아무거나 사용해도 상관없다.
my_name = "Jose"
print(my_name)

my_name2 = 'Bath'
print(my_name2)

# ======= Boolean =======
# Python에서 Boolean 은 반드시 True, False 로 작성해야 한다.
bool1 = True
bool2 = False

print(bool1)
print(bool2)

# 아래와 같이 작성하면 error 발생
# NameError: name 'true' is not defined. Did you mean: 'True'? 
# bool1 = true
# NameError: name 'false' is not defined. Did you mean: 'False'?
# bool2 = false